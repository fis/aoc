func TestReassemble(t *testing.T) {
	set, err := parseSet(example)
	if err != nil {
		t.Fatalf("parseSet: %v", err)
	}
	want := 20899048083289
	if _, _, got, err := set.reassemble(); err != nil {
		t.Errorf("reassemble: %v", err)
	} else if got != want {
		t.Errorf("reassemble = %d, want %d", got, want)
	}
}

func TestRoughness(t *testing.T) {
	set, err := parseSet(example)
	if err != nil {
		t.Fatalf("parseSet: %v", err)
	}
	img, size, _, err := set.reassemble()
	if err != nil {
		t.Fatalf("reassemble: %v", err)
	}
	want := 273
	got := roughness(img, size)
	if got != want {
		t.Errorf("roughness = %d, want %d", got, want)
	}
}
